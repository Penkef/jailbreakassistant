def get_file_hash(file_path):
    """Calculer le hash SHA-256 d'un fichier"""
    try:
        with open(file_path, 'rb') as f:
            return hashlib.sha256(f.read()).hexdigest()
    except:
        return None

def get_all_local_files():
    """Obtenir tous les fichiers locaux √† synchroniser (exclut les fichiers temporaires et caches)"""
    excluded_patterns = [
        '__pycache__',
        '.git',
        'node_modules',
        '.env',
        'access.log',
        'backup_*',
        '.DS_Store',
        '*.pyc',
        '*.pyo',
        '*.tmp',
        'attached_assets'
    ]

    all_files = []

    for root, dirs, files in os.walk('.'):
        # Filtrer les dossiers √† exclure
        dirs[:] = [d for d in dirs if not any(pattern in d for pattern in excluded_patterns)]

        for file in files:
            file_path = os.path.join(root, file)[2:]  # Retirer le './' du d√©but

            # Exclure les fichiers selon les patterns
            if not any(pattern.replace('*', '') in file_path for pattern in excluded_patterns):
                all_files.append(file_path)

    return sorted(all_files)

def get_all_github_files(repo, branch, headers):
    """Obtenir tous les fichiers du repository GitHub de mani√®re r√©cursive"""
    all_files = []

    def get_tree_recursive(tree_sha):
        """R√©cup√©rer l'arbre des fichiers de mani√®re r√©cursive"""
        tree_url = f"https://api.github.com/repos/{repo}/git/trees/{tree_sha}?recursive=1"

        try:
            response = requests.get(tree_url, headers=headers, timeout=15)
            if response.status_code == 200:
                tree_data = response.json()
                for item in tree_data.get('tree', []):
                    if item['type'] == 'blob':  # C'est un fichier
                        all_files.append(item['path'])
        except Exception as e:
            print(f"Erreur lors de la r√©cup√©ration de l'arbre: {e}")

    try:
        # Obtenir le SHA du commit le plus r√©cent
        commits_url = f"https://api.github.com/repos/{repo}/commits/{branch}"
        response = requests.get(commits_url, headers=headers, timeout=10)

        if response.status_code == 200:
            commit_data = response.json()
            tree_sha = commit_data['commit']['tree']['sha']
            get_tree_recursive(tree_sha)
        else:
            print(f"Erreur lors de la r√©cup√©ration des commits: {response.status_code}")

    except Exception as e:
        print(f"Erreur lors de la r√©cup√©ration des fichiers GitHub: {e}")

    return sorted(all_files)

def backup_local_files():
    """Cr√©er une sauvegarde des fichiers locaux avant extraction"""
    backup_dir = f"backup_{datetime.now().strftime('%Y%m%d_%H%M%S')}"

    # Utiliser tous les fichiers locaux pour la sauvegarde
    files_to_backup = get_all_local_files()

    backup_created = False

    for file_path in files_to_backup:
        if os.path.exists(file_path):
            if not backup_created:
                os.makedirs(backup_dir, exist_ok=True)
                backup_created = True

            # Cr√©er les dossiers de sauvegarde si n√©cessaire
            backup_file_path = os.path.join(backup_dir, file_path)
            os.makedirs(os.path.dirname(backup_file_path), exist_ok=True)

            # Copier le fichier
            shutil.copy2(file_path, backup_file_path)

    if backup_created:
        print(f"üíæ Sauvegarde cr√©√©e dans: {backup_dir}")
        return backup_dir
    else:
        print("‚ÑπÔ∏è Aucun fichier local √† sauvegarder")
        return None

def compare_files(local_path, github_content_b64):
    """Comparer un fichier local avec le contenu GitHub"""
    if not os.path.exists(local_path):
        return "local_missing"

    try:
        with open(local_path, 'rb') as f:
            local_content = f.read()
            local_b64 = base64.b64encode(local_content).decode()

        github_content_clean = github_content_b64.replace('\n', '')

        if local_b64 == github_content_clean:
            return "identical"
        else:
            return "different"
    except:
        return "error"

def pull_from_github(force=False):
    """Extraire les fichiers depuis GitHub avec comparaison intelligente"""
    try:
        github_token = os.getenv('GITHUB_TOKEN')
        github_repo = os.getenv('GITHUB_REPO')
        github_branch = os.getenv('GITHUB_BRANCH', 'main')

        if not github_token or not github_repo:
            print("‚ö†Ô∏è Variables GitHub manquantes (GITHUB_TOKEN, GITHUB_REPO)")
            print("V√©rifiez votre fichier .env")
            return False

        headers = {
            "Authorization": f"token {github_token}",
            "Accept": "application/vnd.github.v3+json",
        }

        # Cr√©er une sauvegarde avant extraction
        backup_dir = backup_local_files() if not force else None

        # Obtenir tous les fichiers du repository GitHub
        files_to_pull = get_all_github_files(github_repo, github_branch, headers)

        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        files_downloaded = 0
        files_created = 0
        files_skipped = 0
        conflicts = []

        print(f"‚¨áÔ∏è D√©but d'extraction depuis GitHub - {timestamp}")
        print(f"Repository: {github_repo}")
        print(f"Branche: {github_branch}")
        if backup_dir:
            print(f"üíæ Sauvegarde: {backup_dir}")
        print("-" * 50)

        for file_path in files_to_pull:
            try:
                api_url = f"https://api.github.com/repos/{github_repo}/contents/{file_path}"

                response = requests.get(api_url, headers=headers, timeout=15)

                if response.status_code == 200:
                    file_data = response.json()
                    content_b64 = file_data.get('content', '')

                    if content_b64:
                        try:
                            # Comparer avec le fichier local
                            comparison = compare_files(file_path, content_b64)

                            if comparison == "identical" and not force:
                                print(f"üìã {file_path} - Identique (ignor√©)")
                                files_skipped += 1
                                continue
                            elif comparison == "different" and not force:
                                print(f"‚ö†Ô∏è {file_path} - CONFLIT D√âTECT√â!")
                                print(f"   Local diff√©rent de GitHub. Utilisez --force pour √©craser.")
                                conflicts.append(file_path)
                                continue

                            # D√©coder et √©crire le fichier
                            file_content = base64.b64decode(content_b64)

                            # Cr√©er les dossiers si n√©cessaire
                            os.makedirs(os.path.dirname(file_path), exist_ok=True)

                            file_exists = os.path.exists(file_path)

                            with open(file_path, 'wb') as f:
                                f.write(file_content)

                            if file_exists:
                                print(f"‚úÖ {file_path} - Mis √† jour")
                                files_downloaded += 1
                            else:
                                print(f"üÜï {file_path} - Cr√©√©")
                                files_created += 1

                        except Exception as decode_error:
                            print(f"‚ùå Erreur d√©codage {file_path}: {str(decode_error)}")
                    else:
                        print(f"‚ö†Ô∏è {file_path} - Contenu vide")

                elif response.status_code == 404:
                    print(f"‚ö†Ô∏è {file_path} - Non trouv√© sur GitHub")
                else:
                    print(f"‚ùå {file_path} - Erreur {response.status_code}")

            except Exception as e:
                print(f"‚ùå Erreur pour {file_path}: {str(e)}")

        print(f"\nüìä R√©sum√© d'extraction:")
        print(f"   ‚Ä¢ Fichiers mis √† jour: {files_downloaded}")
        print(f"   ‚Ä¢ Fichiers cr√©√©s: {files_created}")
        print(f"   ‚Ä¢ Fichiers ignor√©s: {files_skipped}")
        print(f"   ‚Ä¢ Conflits d√©tect√©s: {len(conflicts)}")
        print(f"   ‚Ä¢ Total trait√©: {files_downloaded + files_created + files_skipped + len(conflicts)}/{len(files_to_pull)}")

        if conflicts:
            print(f"\n‚ö†Ô∏è Fichiers en conflit:")
            for conflict in conflicts:
                print(f"   ‚Ä¢ {conflict}")
            print(f"Utilisez: python github_sync.py pull --force pour √©craser")

        if backup_dir:
            print(f"\nüíæ Sauvegarde disponible dans: {backup_dir}")

        print("=" * 50)
        return True

    except Exception as e:
        print(f"‚ö†Ô∏è Erreur g√©n√©rale d'extraction: {str(e)}")
        return False

def sync_with_github():
    """Synchroniser les fichiers locaux vers GitHub (PUSH)"""
    try:
        github_token = os.getenv('GITHUB_TOKEN')
        github_repo = os.getenv('GITHUB_REPO')
        github_branch = os.getenv('GITHUB_BRANCH', 'main')

        if not github_token or not github_repo:
            print("‚ö†Ô∏è Variables GitHub manquantes (GITHUB_TOKEN, GITHUB_REPO)")
            print("V√©rifiez votre fichier .env")
            return False

        headers = {
            "Authorization": f"token {github_token}",
            "Accept": "application/vnd.github.v3+json",
        }

        # Test de connexion
        test_url = f"https://api.github.com/repos/{github_repo}"
        try:
            test_response = requests.get(test_url, headers=headers, timeout=10)
            if test_response.status_code == 404:
                print(f"‚ùå Repository '{github_repo}' non trouv√©")
                return False
            elif test_response.status_code != 200:
                print(f"‚ùå Erreur d'acc√®s: {test_response.status_code}")
                return False
        except requests.exceptions.RequestException as e:
            print(f"‚ùå Connexion GitHub impossible: {str(e)}")
            return False

        # Obtenir tous les fichiers locaux √† synchroniser
        files_to_sync = get_all_local_files()

        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        files_updated = 0
        files_created = 0
        files_skipped = 0

        print(f"‚¨ÜÔ∏è Synchronisation vers GitHub - {timestamp}")
        print(f"Repository: {github_repo}")
        print(f"Branche: {github_branch}")
        print("-" * 50)

        for file_path in files_to_sync:
            if not os.path.exists(file_path):
                print(f"‚ö†Ô∏è Fichier non trouv√©: {file_path}")
                continue

            try:
                with open(file_path, "rb") as f:
                    file_content = f.read()
                    content_b64 = base64.b64encode(file_content).decode()

                api_url = f"https://api.github.com/repos/{github_repo}/contents/{file_path}"

                # V√©rifier le fichier existant sur GitHub
                sha = None
                file_exists = False

                try:
                    get_response = requests.get(api_url, headers=headers, timeout=10)

                    if get_response.status_code == 200:
                        remote_file = get_response.json()
                        sha = remote_file.get("sha")
                        file_exists = True

                        # Comparer les contenus
                        remote_content_b64 = remote_file.get("content", "").replace('\n', '')
                        if remote_content_b64 == content_b64:
                            print(f"üìã {file_path} - D√©j√† √† jour")
                            files_skipped += 1
                            continue

                    elif get_response.status_code == 404:
                        file_exists = False

                except requests.exceptions.RequestException:
                    continue

                # Pr√©parer et envoyer la mise √† jour
                data = {
                    "message": f"Auto-sync {file_path} - {timestamp}",
                    "content": content_b64,
                    "branch": github_branch
                }

                if sha:
                    data["sha"] = sha

                try:
                    put_response = requests.put(api_url, headers=headers, json=data, timeout=20)

                    if put_response.status_code in [200, 201]:
                        if file_exists:
                            print(f"‚úÖ {file_path} - Mis √† jour")
                            files_updated += 1
                        else:
                            print(f"üÜï {file_path} - Cr√©√©")
                            files_created += 1
                    else:
                        print(f"‚ùå {file_path} - √âchec: {put_response.status_code}")

                except requests.exceptions.RequestException as e:
                    print(f"‚ùå Erreur envoi {file_path}: {str(e)}")

            except Exception as e:
                print(f"‚ùå Erreur traitement {file_path}: {str(e)}")

        print(f"\nüìä R√©sum√© de synchronisation:")
        print(f"   ‚Ä¢ Fichiers mis √† jour: {files_updated}")
        print(f"   ‚Ä¢ Fichiers cr√©√©s: {files_created}")
        print(f"   ‚Ä¢ Fichiers inchang√©s: {files_skipped}")
        print(f"   ‚Ä¢ Total trait√©: {files_updated + files_created + files_skipped}/{len(files_to_sync)}")
        print("=" * 50)
        return True

    except Exception as e:
        print(f"‚ö†Ô∏è Erreur de synchronisation: {str(e)}")
        return False

